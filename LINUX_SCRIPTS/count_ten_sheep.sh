validity=0
echo "Count one sheep?"

while [ $validity -lt 1 ]
do
    echo "***Type \"y\" or \"n\"***"
    read response

    case $response in
    y)
    ((count++))
    ((validity++))
    ;; Y)
    ((count++))
    ((validity++))
    ;; n)
    echo "$count" >> count.txt
    echo ""
    echo "Program concluded. Please check \"count.txt\" to see the number of sheep you counted!"
    echo ""
    exit 0
    ((validity++))
    ;; N)
    echo "$count" >> count.txt
    echo ""
    echo "Program concluded. Please check \"count.txt\" to see the number of sheep you counted!"
    echo ""
    exit 0
